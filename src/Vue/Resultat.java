    public void initTable()
    {
        Object o[] = new Object[3];
        for(Try l:list)
        {
            o[0]=l.getDate();
            o[1]=l.getRDay();
            o[2]=l.getTDay();
            model.addRow(o); 
        }
       if(!list.isEmpty())
       {
           jLabel3.setText(""+nbr_all);
           jLabel4.setText(""+nbr_true);
       }
    }

